package Shape;

public  class tetrahedron { 
	
	// dörtgen 
protected double h;
protected double x;
public tetrahedron(double h, double x) {
	super();
	this.h = h;
	this.x = x;
}
public double getH() {
	return h;
}
public void setH(double h) {
	this.h = h;
}
public double getX() {
	return x;
}
public void setX(double x) {
	this.x = x;
}


	
}
